package com.dmp.beans;


public class Lit {

  private int idLit;

  private int codeLit;

public Lit() {
	super();
}

public Lit(int codeLit) {
	super();
	this.codeLit = codeLit;
}

public int getIdLit() {
	return idLit;
}

public void setIdLit(int idLit) {
	this.idLit = idLit;
}

public int getCodeLit() {
	return codeLit;
}

public void setCodeLit(int codeLit) {
	this.codeLit = codeLit;
}

 
}